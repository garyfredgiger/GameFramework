package game.underdevelopment.utilities.matrix;

/*
 * This is a container for the 3 x 3 matrix elements
 */
public class Matrix3By3
{
  public double element11, element12, element13;
  public double element21, element22, element23;
  public double element31, element32, element33;

  public Matrix3By3()
  {
    element11 = 0.0;
    element12 = 0.0;
    element13 = 0.0;
    element21 = 0.0;
    element22 = 0.0;
    element23 = 0.0;
    element31 = 0.0;
    element32 = 0.0;
    element33 = 0.0;
  }
}
